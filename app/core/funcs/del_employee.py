"""
Модуль функций для удаления сотрудников.

Этот модуль предоставляет функцию для удаления сотрудника
из хранилища данных по его идентификатору. Функция также
возвращает данные удаленного сотрудника для возможного
использования или отображения информации.
"""

from app.core.db.database import Storage


def func_del_emp(id: str):
    """
    Удаляет сотрудника из хранилища данных по его идентификатору.
    
    Функция получает идентификатор сотрудника, находит соответствующую
    запись в хранилище, удаляет ее и возвращает данные удаленного
    сотрудника.
    
    Args:
        id (str): Идентификатор сотрудника для удаления
        
    Returns:
        dict: Данные удаленного сотрудника в виде словаря
        
    Note:
        Если сотрудник с указанным идентификатором не найден,
        результат может быть None или пустым словарем.
    """
    with Storage() as s:
        emp = s.get_by_id(id)
        s.delete(id)

    return emp